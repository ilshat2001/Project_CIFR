import streamlit as st
import reveal_slides as rs

def presentation_page():
    st.title("Презентация проекта")
    
    presentation_markdown = """
# Прогнозирование отказов оборудования

## Введение
- Задача: предсказать отказ оборудования (Target = 1) или его отсутствие (Target = 0)
- Датасет: AI4I 2020 Predictive Maintenance Dataset (10,000 записей, 14 признаков)
- Цель: снижение затрат на обслуживание оборудования

---

## Этапы работы
1. Загрузка и предобработка данных
   - Удаление ненужных столбцов
   - Преобразование категориальных переменных
   - Масштабирование числовых признаков
2. Обучение моделей
   - Logistic Regression
   - Random Forest
   - XGBoost
3. Оценка и сравнение моделей
4. Создание Streamlit-приложения

---

## Результаты
- Лучшая модель: Random Forest (Accuracy: 0.95, ROC-AUC: 0.98)
- Приложение позволяет:
  - Загружать данные
  - Обучать модели
  - Визуализировать результаты
  - Делать предсказания

---

## Заключение
- Реализована система предсказания отказов оборудования
- Достигнута высокая точность предсказаний
- Возможные улучшения:
  - Добавление новых признаков
  - Использование нейронных сетей
  - Интеграция с промышленными системами
"""
    
    rs.slides(
        presentation_markdown,
        height=500,
        theme="black",
        config={
            "transition": "slide",
            "plugins": ["highlight", "zoom"]
        }
    )